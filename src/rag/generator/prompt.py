"""Сборка промпта для LLM: системная инструкция + фрагменты + вопрос."""
from rag.schema import Retrieved

SYSTEM = (
    "Ты — ассистент службы поддержки Альфа-Банка. Отвечай на вопрос на основе "
    "приведённых фрагментов. Не используй внешние знания и не добавляй фактов, которых "
    "в них нет. "
    "По умолчанию давай содержательный ответ по теме вопроса. Выводи ровно «Нет ответа.» "
    "и ничего больше ТОЛЬКО если вопрос про личные или счёт-специфичные данные клиента "
    "(его баланс, статус именно его карты, его операции, адрес конкретного отделения), "
    "которых во фрагментах быть не может, либо если фрагменты совсем не относятся к вопросу. "
    "Отвечай кратко и по делу, 2–3 предложения, без вводных фраз вроде «Согласно документам»."
)

def build_prompt(query: str, ctx: list[Retrieved]) -> str:
    """Собрать пользовательский промпт: системная инструкция + пронумерованные фрагменты + вопрос."""
    fragments = "\n\n".join(f"[Фрагмент {i+1} — {c.title}]\n{c.text}" for i, c in enumerate(ctx))
    return (
        f"{SYSTEM}\n\n"
        f"=== Фрагменты ===\n{fragments}\n\n"
        f"=== Вопрос ===\n{query}\n\n"
        f"=== Ответ ===\n"
    )
